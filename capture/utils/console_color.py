#!/usr/bin/env python
# -*- coding: utf-8 -*-

"""Colors for termial outputs"""


class ConsoleColor(object):
    """Simple shortcut to use colors in console"""
    HEADER = '\033[95m'
    BLUE = '\033[94m'
    GREEN = '\033[92m'
    ORANGE = '\033[93m'
    RED = '\033[91m'
    ENDCOLOR = '\033[0m'
    BOLD = '\033[1m'
    UNDERLINE = '\033[4m'
